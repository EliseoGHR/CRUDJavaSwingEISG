    private boolean validarDatos() {
        boolean valid = true;

        if (jTxtNombre.getText().isEmpty()) {
            valid = false;
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio", "Validar campo", JOptionPane.WARNING_MESSAGE);
        }

        if (jTxtDuracion.getText().isEmpty()) {
            valid = false;
            JOptionPane.showMessageDialog(this, "La duracion es obligatoria", "Validar campo", JOptionPane.WARNING_MESSAGE);
        }
        if (jTxtPrecio.getText().isEmpty()) {
            valid = false;
            JOptionPane.showMessageDialog(this, "El precio es obligatorio", "Validar campo", JOptionPane.WARNING_MESSAGE);
        }

        return valid;
    }

    private Curso obtenerDatos() {
        Curso curso = new Curso();
        curso.setNombre(jTxtNombre.getText());
        curso.setDescripcion(jTxtADescripcion.getText());
        curso.setDuracion(Integer.parseInt(jTxtDuracion.getText()));
        curso.setPrecio(Double.parseDouble(jTxtPrecio.getText()));
        curso.setCursoID(cursoActual.getCursoID());
        return curso;
    }

    private void asingarDatos(Curso curso) {
        jTxtNombre.setText(curso.getNombre());
        jTxtADescripcion.setText(curso.getDescripcion());
        jTxtDuracion.setText(Integer.toString(curso.getDuracion()));
        jTxtPrecio.setText(Double.toString(curso.getPrecio()));

    }

    private void crearReg() {
        try {
            Curso curso = obtenerDatos();
            int result = CursoDAL.crear(curso);
            if (result > 0) {
                JOptionPane.showMessageDialog(this,
                        "El curso fue registrado existosamente", "CREAR CURSO",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                        "Sucedio un error al crear el curso", "ERROR CURSO",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR CURSO",
                    JOptionPane.ERROR_MESSAGE);
        }

    }

    private void modificarReg() {
        try {
            Curso curso = obtenerDatos();
            int result = CursoDAL.modificar(curso);
            if (result > 0) {
                JOptionPane.showMessageDialog(this,
                        "El curso fue modificado existosamente", "MODIFICAR CURSO",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                        "Sucedio un error al modificar el curso", "ERROR CURSO",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR CURSO",
                    JOptionPane.ERROR_MESSAGE);
        }

    }

    private void eliminarReg() {
        try {
            Curso curso = obtenerDatos();
            int result = CursoDAL.eliminar(curso);
            if (result > 0) {
                JOptionPane.showMessageDialog(this,
                        "El curso fue eliminado existosamente", "ELIMINAR CURSO",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                        "Sucedio un error al eliminar el curso", "ERROR CURSO",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR CURSO",
                    JOptionPane.ERROR_MESSAGE);
        }

    }
    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:
        FrmCursosLec frmCursosLec = new FrmCursosLec();
        if (validarDatos()) {
            if (null != opcionCRUD) {
                switch (opcionCRUD) {
                    case CREAR:
                        crearReg();
                        frmCursosLec.setVisible(true);
                        this.setVisible(false);
                        break;
                    case MODIFICAR:
                        modificarReg();
                        frmCursosLec.setVisible(true);
                        this.setVisible(false);
                        break;
                    case ELIMINAR:
                        eliminarReg();
                        frmCursosLec.setVisible(true);
                        this.setVisible(false);
                        break;
                    default:
                        break;
                }
            }
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

